int findinstruction(enum mnemonic_type mn,struct operand op[]){
    for(int i=0;i<256;i++){
        if(mn!=all_instructions[i].mnemonic){
            continue;
        }
        int j;
        for(j=0;j<3;j++){
            enum operand_type search = all_instructions[i].operands[j];
            if(op[j].type==op_label){   // labels can replace offset,addr16,addr11
                if(search<=op_offset && search>=op_addr11){
                    continue;
                }
            }
            if(op[j].type!=all_instructions[i].operands[j]){
                break;
            }
        }
        if(j==3){
            return i;   // returns instruction address
        }
    }
    return 0xA5;    // no instruction found
}
